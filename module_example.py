# Python module example

def add(a, b):
    """This program adds two
    numbers and return the result"""

    result = a + b
    return result


def sub(a, b):
    """This program subtracts two
    numbers and return the result"""

    result = a - b
    return result


def mul(a, b):
    """This program multiply two
    numbers and return the result"""

    result = a * b
    return result


def div(a, b):
    """This program division two
    numbers and return the result"""

    result = a / b
    return result
